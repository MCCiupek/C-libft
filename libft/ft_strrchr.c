char	*ft_strrchr(char *str, char c)
{
	int	i;

	i = 0;
	while (str[i])
		i++;
	if (!c)
		return (&str[i]);
	while (i-- >= 0)
		if (str[i] == c)
			return (&str[i]);
	return (0);
}
