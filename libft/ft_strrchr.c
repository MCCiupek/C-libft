char *ft_sttrchr(char *str, char c)
{
	int	i;
	int	len;

	i = 0;
	len = 0;
	while (str[++i])
		len++;
	i = len;
	while (str[--i])
		if (str[i] == c)
			return (&str[i]);
	return (0);
}
